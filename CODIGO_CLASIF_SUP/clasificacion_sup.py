    with rasterio.open(raster_path) as src:
        data = src.read(1)
        bounds = src.bounds

        # Crear visualización base (todas las clases con transparencia)
        rgb_base = np.zeros((data.shape[0], data.shape[1], 3))
        for code, info in WORLDCOVER_CLASSES.items():
            if code != 0 and code in data:
                mask = data == code
                hex_color = info["color"].lstrip('#')
                color_rgb = [int(hex_color[i:i+2], 16)/255 for i in (0, 2, 4)]
                rgb_base[mask] = color_rgb

        # Mostrar base con transparencia
        ax.imshow(rgb_base, extent=[bounds.left, bounds.right, bounds.bottom, bounds.top], alpha=0.3)

        # Resaltar la clase específica
        class_mask = (data == class_code)
        if np.any(class_mask):
            rgb_highlight = np.zeros((data.shape[0], data.shape[1], 4))  # RGBA
            hex_color = class_info["color"].lstrip('#')
            color_rgb = [int(hex_color[i:i+2], 16)/255 for i in (0, 2, 4)]

            # Crear canal alpha: 1 donde está la clase, 0 elsewhere
            rgb_highlight[class_mask] = [*color_rgb, 0.8]  # RGBA con opacidad
            rgb_highlight[~class_mask] = [0, 0, 0, 0]  # Transparente

            ax.imshow(rgb_highlight, extent=[bounds.left, bounds.right, bounds.bottom, bounds.top])

            # Calcular estadísticas
            total_pixels = np.sum(data != 0)
            class_pixels = np.sum(class_mask)
            percentage = (class_pixels / total_pixels) * 100
            area_km2 = class_pixels * (src.res[0] * src.res[1]) / 1e6

            print(f"📊 Estadísticas de {class_info['name']}:")
            print(f"   Píxeles: {class_pixels:,}")
            print(f"   Porcentaje del área: {percentage:.1f}%")
            print(f"   Área aproximada: {area_km2:.2f} km²")

        # Si hay puntos muestreados, mostrarlos también
        if samples_gdf is not None:
            class_points = samples_gdf[samples_gdf['class'] == class_code]
            if not class_points.empty:
                hex_color = class_info["color"].lstrip('#')
                rgb_color = [int(hex_color[i:i+2], 16)/255 for i in (0, 2, 4)]

                class_points.plot(ax=ax, color=rgb_color, markersize=60,
                                edgecolor='white', linewidth=2, alpha=0.9,
                                label=f"Puntos muestreados ({len(class_points)})")

    ax.set_title(f'Clase {class_code}: {class_info["name"]}', fontsize=16, fontweight='bold')
    ax.set_xlabel('Longitud')
    ax.set_ylabel('Latitud')

    # Crear leyenda manual
    from matplotlib.patches import Patch
