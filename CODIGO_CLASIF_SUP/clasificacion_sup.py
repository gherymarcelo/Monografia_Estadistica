def generate_stratified_points(raster_path, points_per_class=100):
    """Genera puntos estratificados por clase"""
    with rasterio.open(raster_path) as src:
        data = src.read(1)
        transform = src.transform

        # Obtener coordenadas para cada clase
        class_points = {}
        for class_code in WORLDCOVER_CLASSES.keys():
            if class_code == 0:
                continue

            # Encontrar píxeles de esta clase
            rows, cols = np.where(data == class_code)
            if len(rows) > 0:
                class_points[class_code] = list(zip(rows, cols))

        # Muestrear puntos
        samples = []
        for class_code, coords in class_points.items():
            if len(coords) > points_per_class:
                sampled_coords = random.sample(coords, points_per_class)
            else:
                sampled_coords = coords

            for row, col in sampled_coords:
                # Convertir coordenadas de píxel a coordenadas geográficas
                lon, lat = transform * (col, row)
                samples.append({
                    'class': class_code,
                    'geometry': Point(lon, lat),
                    'row': row,
                    'col': col
                })

        return gpd.GeoDataFrame(samples, crs=src.crs)

