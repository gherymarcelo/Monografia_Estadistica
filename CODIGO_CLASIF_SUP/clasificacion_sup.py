def plot_classification_map(classification_map, s2_image):
    """Visualiza el mapa de clasificación - Versión corregida"""
    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(18, 8))

    # Imagen original
    try:
        red_idx = np.where(s2_image.band.values == 'B4')[0][0]
        green_idx = np.where(s2_image.band.values == 'B3')[0][0]
        blue_idx = np.where(s2_image.band.values == 'B2')[0][0]

        rgb = np.stack([s2_image[red_idx].values,
                       s2_image[green_idx].values,
                       s2_image[blue_idx].values], axis=-1)

        rgb = np.nan_to_num(rgb, nan=0)
        p2, p98 = np.percentile(rgb[rgb > 0], (2, 98)) if np.any(rgb > 0) else (0, 1)
        rgb = (rgb - p2) / (p98 - p2 + 1e-10)
        rgb = np.clip(rgb, 0, 1)

        ax1.imshow(rgb)
        ax1.set_title('Sentinel-2 - Color Natural (B4, B3, B2)')
        ax1.axis('off')
    except Exception as e:
        ax1.set_title(f'Error visualización RGB: {str(e)[:50]}...')
        ax1.axis('off')

    # Mapa de clasificación - Crear máscara para valores válidos
    valid_mask = classification_map != -9999
    classification_display = classification_map.copy()

    # Crear imagen RGB para visualización
    rgb_classification = np.zeros((*classification_display.shape, 3))

    for code, info in WORLDCOVER_CLASSES.items():
        mask = (classification_display == code) & valid_mask
        hex_color = info["color"].lstrip('#')
