def extract_pixel_values(points_gdf, transform, data_arrays, band_names):
    """Extrae valores de píxel para puntos específicos"""
    point_values = []

    for idx, point in points_gdf.iterrows():
        x, y = point.geometry.x, point.geometry.y

        # Convertir coordenadas geográficas a coordenadas de píxel
        col, row = ~transform * (x, y)
        col, row = int(round(col)), int(round(row))

        # Extraer valores de todas las bandas
        values = {'class': point['class']}

        for i, data_array in enumerate(data_arrays):
            if 0 <= row < data_array.shape[1] and 0 <= col < data_array.shape[2]:
                for band_idx in range(data_array.shape[0]):
                    band_name = f"{band_names[i]}_{band_idx+1}"
                    values[band_name] = data_array[band_idx, row, col]
            else:
                # Si está fuera de los límites, asignar NaN
                for band_idx in range(data_array.shape[0]):
                    band_name = f"{band_names[i]}_{band_idx+1}"
                    values[band_name] = np.nan

        point_values.append(values)

    return pd.DataFrame(point_values)

