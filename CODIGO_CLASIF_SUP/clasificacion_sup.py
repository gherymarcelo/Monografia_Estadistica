def class_statistics(raster_path, class_code):
    """Estadísticas detalladas de una clase"""
    if class_code not in WORLDCOVER_CLASSES:
        print(f"Clase {class_code} no válida")
        return

    class_info = WORLDCOVER_CLASSES[class_code]

    with rasterio.open(raster_path) as src:
        data = src.read(1)
        class_mask = (data == class_code)

        if not np.any(class_mask):
            print(f"❌ La clase {class_code} ({class_info['name']}) no está presente en el área")
            return

        # Cálculos
        total_pixels = np.sum(data != 0)
        class_pixels = np.sum(class_mask)
        percentage = (class_pixels / total_pixels) * 100

        # Área en km²
        pixel_area_m2 = src.res[0] * src.res[1]  # Área de un píxel en m²
        total_area_km2 = (total_pixels * pixel_area_m2) / 1e6
        class_area_km2 = (class_pixels * pixel_area_m2) / 1e6

        # Encontrar bounding box de la clase
        rows, cols = np.where(class_mask)
        if len(rows) > 0:
            min_row, max_row = np.min(rows), np.max(rows)
            min_col, max_col = np.min(cols), np.max(cols)

            # Convertir a coordenadas
            ul = src.transform * (min_col, min_row)
            lr = src.transform * (max_col + 1, max_row + 1)

            extent_km = ((lr[0] - ul[0]) * 111 * np.cos(np.radians(ul[1])),
                        (lr[1] - ul[1]) * 111)

        print(f"📊 ESTADÍSTICAS - {class_code}: {class_info['name']}")
        print("=" * 50)
        print(f"Píxeles de la clase: {class_pixels:,}")
        print(f"Porcentaje del área: {percentage:.2f}%")
        print(f"Área aproximada: {class_area_km2:.2f} km²")
        print(f"Área total del raster: {total_area_km2:.2f} km²")

        if len(rows) > 0:
            print(f"Extensión aproximada: {extent_km[0]:.2f} km x {extent_km[1]:.2f} km")
            print(f"Coordenadas del área: {ul[0]:.6f}°, {ul[1]:.6f}° a {lr[0]:.6f}°, {lr[1]:.6f}°")

# Ejemplo: Estadísticas de clase 50
