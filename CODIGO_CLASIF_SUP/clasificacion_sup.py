def generate_classification_map(model, scaler, s2_image, output_path, class_mapping, reverse_class_mapping):
    """Genera mapa de clasificación con el mejor modelo - Versión corregida"""
    print(f"Generando mapa con {best_model_name}...")

    height, width = s2_image.shape[1], s2_image.shape[2]

    # Extraer bandas necesarias
    useful_bands = ['B2', 'B3', 'B4', 'B5', 'B8']
    band_indices = [np.where(s2_image.band.values == band)[0][0] for band in useful_bands]
    useful_image = s2_image[band_indices]

    # Reorganizar datos
    image_flat = useful_image.values.reshape(len(useful_bands), -1).T

    # Crear características
    features_img = pd.DataFrame(image_flat, columns=useful_bands)

    # Normalizar si es necesario
    if features_img.max().max() > 1000:
        features_img[useful_bands] = features_img[useful_bands] / 10000.0

    # Calcular índices
    features_img['NDVI'] = (features_img['B8'] - features_img['B4']) / (features_img['B8'] + features_img['B4'] + 1e-10)
    features_img['EVI'] = 2.5 * (features_img['B8'] - features_img['B4']) / (features_img['B8'] + 6 * features_img['B4'] - 7.5 * features_img['B2'] + 1)
    features_img['NDWI'] = (features_img['B3'] - features_img['B8']) / (features_img['B3'] + features_img['B8'] + 1e-10)
    features_img['B4/B3'] = features_img['B4'] / (features_img['B3'] + 1e-10)

    # Manejar valores infinitos y NaN
    features_img = features_img.replace([np.inf, -np.inf], np.nan)

    # Identificar píxeles válidos (sin NaN)
    valid_mask = ~np.any(np.isnan(features_img[X.columns].values), axis=1)
    print(f"Píxeles válidos: {np.sum(valid_mask)}/{len(valid_mask)}")

    # Seleccionar características en el orden correcto solo para píxeles válidos
    X_img_valid = features_img[X.columns].values[valid_mask]

    # Escalar y predecir solo píxeles válidos
    X_img_scaled_valid = scaler.transform(X_img_valid)
    y_pred_encoded_valid = model.predict(X_img_scaled_valid)

    # Convertir de vuelta a clases originales
    y_pred_original_valid = np.array([reverse_class_mapping[c] for c in y_pred_encoded_valid])

    # Crear imagen completa con -9999 para valores inválidos
    classification_map = np.full(len(valid_mask), -9999, dtype=np.int16)
    classification_map[valid_mask] = y_pred_original_valid

    # Reformar a imagen 2D
    classification_map = classification_map.reshape((height, width))

    # Guardar
    with rasterio.open(
        output_path,
        'w',
        driver='GTiff',
        height=height,
        width=width,
        count=1,
        dtype=rasterio.int16,
        crs=s2_image.rio.crs,
        transform=s2_image.rio.transform(),
        nodata=-9999
    ) as dst:
        dst.write(classification_map.astype(np.int16), 1)

    print(f"✅ Mapa guardado en: {output_path}")
    return classification_map

