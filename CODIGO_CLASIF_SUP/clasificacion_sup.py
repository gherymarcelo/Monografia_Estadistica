        # Encontrar líneas con promedios
        weighted_metrics = {}
        for line in report_lines:
            if 'weighted avg' in line:
                parts = line.split()
                if len(parts) >= 6:
                    try:
                        weighted_metrics['precision'] = float(parts[2])
                        weighted_metrics['recall'] = float(parts[3])
                        weighted_metrics['f1_score'] = float(parts[4])
                    except:
                        continue

        comparison_data.append({
            'Modelo': model_name,
            'Accuracy': accuracy,
            'Precision (Avg)': weighted_metrics.get('precision', 0),
            'Recall (Avg)': weighted_metrics.get('recall', 0),
            'F1-Score (Avg)': weighted_metrics.get('f1_score', 0)
        })

