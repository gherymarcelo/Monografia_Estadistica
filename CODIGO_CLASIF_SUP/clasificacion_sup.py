def train_classification_model(features_df, class_mapping):
    """Entrena y evalúa modelos de clasificación"""
    X = features_df[feature_names].values
    y = features_df['class_encoded'].values

    # Dividir datos
    X_train, X_test, y_train, y_test = train_test_split(X, y, test_size=0.3,
                                                        random_state=42, stratify=y)

    # Escalar características
    scaler = StandardScaler()
    X_train_scaled = scaler.fit_transform(X_train)
    X_test_scaled = scaler.transform(X_test)

    # Modelos a probar
    models = {
        'RandomForest': RandomForestClassifier(n_estimators=100, random_state=42),
        'ExtraTrees': ExtraTreesClassifier(n_estimators=100, random_state=42),
        'XGBoost': XGBClassifier(random_state=42),
        'LightGBM': LGBMClassifier(random_state=42)
    }

    results = {}

    for name, model in models.items():
        print(f"\nEntrenando {name}...")
        model.fit(X_train_scaled, y_train)

        # Predecir
        y_pred = model.predict(X_test_scaled)

        # Convertir de vuelta a clases originales para el reporte
        y_test_original = np.array([reverse_class_mapping[c] for c in y_test])
        y_pred_original = np.array([reverse_class_mapping[c] for c in y_pred])

        # Evaluar
        accuracy = accuracy_score(y_test_original, y_pred_original)
        results[name] = {
            'model': model,
            'accuracy': accuracy,
            'report': classification_report(y_test_original, y_pred_original, zero_division=0)
        }

        print(f"{name} Accuracy: {accuracy:.4f}")

        # Matriz de confusión
        plt.figure(figsize=(10, 8))
        cm = confusion_matrix(y_test_original, y_pred_original, labels=list(class_mapping.keys()))
        sns.heatmap(cm, annot=True, fmt='d', cmap='Blues',
                   xticklabels=[f"{k}\n{WORLDCOVER_CLASSES[k]['name'][:15]}..." for k in class_mapping.keys()],
                   yticklabels=[f"{k}\n{WORLDCOVER_CLASSES[k]['name'][:15]}..." for k in class_mapping.keys()])
        plt.title(f'Matriz de Confusión - {name}')
        plt.ylabel('Verdadero')
        plt.xlabel('Predicho')
        plt.xticks(rotation=45)
        plt.yticks(rotation=0)
        plt.tight_layout()
        plt.show()

    # Seleccionar el mejor modelo
    best_model_name = max(results.items(), key=lambda x: x[1]['accuracy'])[0]
    best_model = results[best_model_name]['model']

    print(f"\nMejor modelo: {best_model_name} con accuracy: {results[best_model_name]['accuracy']:.4f}")
    print("\nReporte de clasificación del mejor modelo:")
    print(results[best_model_name]['report'])

    return best_model, scaler, results, class_mapping, reverse_class_mapping

