# 8. CLASIFICACIÓN DE TODA LA IMAGEN
def classify_full_image(model, scaler, s1_asc, s1_desc, s1_asc_meta, class_mapping, reverse_class_mapping):
    """Clasifica toda la imagen Sentinel-1"""
    print("Clasificando imagen completa...")

    # Preparar datos para clasificación
    height, width = s1_asc.shape[1], s1_asc.shape[2]

    # Reorganizar datos
    asc_vv_flat = s1_asc[0].flatten()
    asc_vh_flat = s1_asc[1].flatten()
    desc_vv_flat = s1_desc[0].flatten()
    desc_vh_flat = s1_desc[1].flatten()

    # Crear matriz de características
    X_full = np.column_stack([asc_vv_flat, asc_vh_flat, desc_vv_flat, desc_vh_flat])

    # Identificar píxeles válidos (no NaN)
    valid_mask = ~np.any(np.isnan(X_full), axis=1)
    X_valid = X_full[valid_mask]

    print(f"Píxeles válidos para clasificación: {len(X_valid)}/{len(X_full)}")

    # Escalar características
    X_valid_scaled = scaler.transform(X_valid)

    # Predecir
    print("Realizando predicciones...")
    y_pred_valid_encoded = model.predict(X_valid_scaled)

    # Convertir de vuelta a clases originales
    y_pred_valid = np.array([reverse_class_mapping[c] for c in y_pred_valid_encoded])

    # Crear imagen de resultados
    result_image = np.full(len(X_full), -9999, dtype=np.int16)  # -9999 para valores no clasificados
    result_image[valid_mask] = y_pred_valid

    # Reformar a la forma original
    classification_result = result_image.reshape((height, width))

    return classification_result

# Clasificar imagen completa
