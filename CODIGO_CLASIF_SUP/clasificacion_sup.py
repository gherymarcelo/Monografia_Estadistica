def create_classification_raster():
    """Crea un raster de clasificación aplicando el modelo a todas las imágenes"""
    print("Procesando imágenes para clasificación completa...")

    # Cargar todas las imágenes nuevamente
    s1_asc, s1_asc_transform, s1_desc, s1_desc_transform, s2_data, s2_transform, s2_crs = load_and_preprocess_data()

    # Obtener dimensiones de la imagen Sentinel-2 (usaremos esta como base)
    height, width = s2_data.shape[1], s2_data.shape[2]
    print(f"Dimensiones del raster: {height} x {width} píxeles")

    # Crear array para la clasificación final
    classification_result = np.zeros((height, width), dtype=np.uint8)

    # Procesar por bloques para evitar problemas de memoria
    block_size = 1000  # Tamaño del bloque para procesamiento
    total_blocks = ((height - 1) // block_size + 1) * ((width - 1) // block_size + 1)
    current_block = 0

    print(f"Procesando en bloques de {block_size}x{block_size}...")

    for y in range(0, height, block_size):
        for x in range(0, width, block_size):
            current_block += 1
            print(f"Procesando bloque {current_block}/{total_blocks}...")

            # Definir límites del bloque actual
            y_end = min(y + block_size, height)
            x_end = min(x + block_size, width)

            # Extraer datos para el bloque actual de todas las imágenes
            block_data = []

            # Extraer datos de Sentinel-1 ASC
            s1_asc_block = s1_asc[:, y:y_end, x:x_end]
            for band in range(s1_asc_block.shape[0]):
                block_data.append(s1_asc_block[band, :, :])

            # Extraer datos de Sentinel-1 DESC
            s1_desc_block = s1_desc[:, y:y_end, x:x_end]
            for band in range(s1_desc_block.shape[0]):
                block_data.append(s1_desc_block[band, :, :])

            # Extraer datos de Sentinel-2
            s2_block = s2_data[:, y:y_end, x:x_end]
            for band in range(s2_block.shape[0]):
                block_data.append(s2_block[band, :, :])

            # Crear array de características para el bloque
            block_features = np.stack(block_data, axis=-1)
            original_shape = block_features.shape[:2]

            # Reformatear para predicción
            X_block = block_features.reshape(-1, block_features.shape[2])

            # Eliminar píxeles con valores NaN o cero
            valid_mask = ~np.any(np.isnan(X_block) | (X_block == 0), axis=1)
            X_valid = X_block[valid_mask]

            if len(X_valid) > 0:
                # Escalar y predecir
                X_valid_scaled = scaler.transform(X_valid)
                y_pred_block = best_model.predict(X_valid_scaled)

                # Convertir a clases originales
                y_pred_original = np.vectorize(reverse_class_mapping.get)(y_pred_block)

                # Asignar resultados al bloque
                result_block = np.zeros(original_shape[0] * original_shape[1], dtype=np.uint8)
                result_block[valid_mask] = y_pred_original
                result_block[~valid_mask] = 0  # Asignar 0 a píxeles no válidos

                classification_result[y:y_end, x:x_end] = result_block.reshape(original_shape)
            else:
                # Si no hay píxeles válidos, asignar 0
                classification_result[y:y_end, x:x_end] = 0

    return classification_result, s2_transform, s2_crs

