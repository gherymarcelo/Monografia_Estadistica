def extract_sentinel_values(points_gdf, s1_data, transform, crs):
    """Extrae valores de Sentinel-1 para los puntos dados"""
    points = points_gdf.to_crs(crs).copy()

    sentinel_values = []

    for idx, point in points.iterrows():
        x, y = point.geometry.x, point.geometry.y

        # Convertir coordenadas geográficas a coordenadas de píxel
        col, row = ~transform * (x, y)
        col, row = int(round(col)), int(round(row))

        # Verificar que las coordenadas estén dentro de la imagen
        if (0 <= row < s1_data.shape[1] and 0 <= col < s1_data.shape[2]):
            # Extraer valores de todas las bandas
            values = []
            for band in range(s1_data.shape[0]):
                value = s1_data[band, row, col]
                values.append(value)
            sentinel_values.append(values)
        else:
            sentinel_values.append([np.nan] * s1_data.shape[0])

    return np.array(sentinel_values)

