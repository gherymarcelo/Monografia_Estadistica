def verify_and_save_samples(samples_gdf, raster_path, output_filename):
    """Verificación final y guardado de puntos"""
    if samples_gdf is None or len(samples_gdf) == 0:
        print("No hay puntos para verificar")
        return

    print("🔍 VERIFICACIÓN FINAL")
    print("=" * 40)

    # 1. Verificar que todos los puntos están dentro del raster
    with rasterio.open(raster_path) as src:
        raster_bounds = src.bounds
        data = src.read(1)
        transform = src.transform

    points_inside = 0
    points_correct_class = 0

    for idx, point in samples_gdf.iterrows():
        x, y = point.geometry.x, point.geometry.y

        # Verificar dentro de bounds
        if (raster_bounds.left <= x <= raster_bounds.right and
            raster_bounds.bottom <= y <= raster_bounds.top):
            points_inside += 1

            # Verificar clase correcta
            try:
                # Corrección: usar la transformación correcta
                col, row = ~transform * (x, y)
                col, row = int(round(col)), int(round(row))

                if 0 <= row < data.shape[0] and 0 <= col < data.shape[1]:
                    if data[row, col] == point['class']:
                        points_correct_class += 1
                    else:
                        print(f"⚠️  Punto {idx}: Clase esperada {point['class']}, pero raster tiene {data[row, col]}")
                else:
                    print(f"⚠️  Punto {idx}: Coordenadas fuera de los límites del array")
            except Exception as e:
                print(f"❌ Error verificando punto {idx}: {e}")

    print(f"Puntos dentro del raster: {points_inside}/{len(samples_gdf)} ({points_inside/len(samples_gdf)*100:.1f}%)")
    print(f"Puntos con clase verificada: {points_correct_class}/{len(samples_gdf)} ({points_correct_class/len(samples_gdf)*100:.1f}%)")

