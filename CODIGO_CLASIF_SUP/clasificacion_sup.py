def extract_s2_values(samples_gdf, s2_image):
    """Extrae valores de Sentinel-2 para puntos"""
    print("Extrayendo valores de bandas...")

    points = samples_gdf.to_crs(s2_image.rio.crs)
    x_coords = s2_image.x.values
    y_coords = s2_image.y.values

    sentinel_values = []
    valid_indices = []

    for idx, point in points.iterrows():
        x, y = point.geometry.x, point.geometry.y

        if not (x_coords.min() <= x <= x_coords.max() and
                y_coords.min() <= y <= y_coords.max()):
            continue

        col_idx = np.abs(x_coords - x).argmin()
        row_idx = np.abs(y_coords - y).argmin()

        values = []
        valid_point = True

        for band in range(s2_image.shape[0]):
            value = s2_image[band, row_idx, col_idx].values
            if np.isnan(value):
                valid_point = False
                break
            values.append(value)

        if valid_point:
            sentinel_values.append(values)
            valid_indices.append(idx)

    print(f"Puntos válidos: {len(sentinel_values)}/{len(points)}")
    return np.array(sentinel_values), valid_indices

